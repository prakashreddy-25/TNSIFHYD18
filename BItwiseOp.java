package com.si.cse;

public class BItwiseOp {
	public static void main(String[] args) {
		int a=10,b=12;
		System.out.println("bitwise AND operation "+(a&b));
		System.out.println("bitwise OR operation "+(a|b));
		System.out.println("bitwise EXOR operation "+(a^b));
		System.out.println("bitwise COMPLIMENT  operation "+(~a));
		System.out.println("bitwise LEFT SHIFT operation "+(a<<b));
		System.out.println("bitwise RIGHT SHIFT operation "+(a>>b));
		
	}

}
